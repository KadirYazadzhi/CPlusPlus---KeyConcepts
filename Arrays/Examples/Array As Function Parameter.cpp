#include <iostream>
using namespace std;

// üßÆ –§—É–Ω–∫—Ü–∏—è –∑–∞ –∏–∑—á–∏—Å–ª—è–≤–∞–Ω–µ –Ω–∞ —Å—É–º–∞—Ç–∞ –Ω–∞ –º–∞—Å–∏–≤
int calculateSum(int arr[], int size) {
    int sum = 0;

    for (int i = 0; i < size; i++) {
        sum += arr[i]; // –î–æ–±–∞–≤—è–Ω–µ –Ω–∞ —Ç–µ–∫—É—â–∏—è –µ–ª–µ–º–µ–Ω—Ç –∫—ä–º —Å—É–º–∞—Ç–∞
    }

    return sum; // –í—Ä—ä—â–∞–Ω–µ –Ω–∞ —Å—É–º–∞—Ç–∞
}

int main() {
    int arr[4] = {5, 10, 15, 20}; // –î–µ–∫–ª–∞—Ä–∏—Ä–∞–Ω–µ –Ω–∞ –º–∞—Å–∏–≤
    int size = 4; // –†–∞–∑–º–µ—Ä –Ω–∞ –º–∞—Å–∏–≤–∞

    // üìä –ü—Ä–∏–∑–æ–≤–∞–≤–∞–Ω–µ –Ω–∞ —Ñ—É–Ω–∫—Ü–∏—è—Ç–∞ –∏ –ø–µ—á–∞—Ç –Ω–∞ —Ä–µ–∑—É–ª—Ç–∞—Ç–∞
    cout << "Sum of the array is: " << calculateSum(arr, size) << endl;
    // –°—É–º–∞—Ç–∞ –Ω–∞ –º–∞—Å–∏–≤–∞ –µ: —Å—Ç–æ–π–Ω–æ—Å—Ç

    return 0;
}
